package ua.training.model;

public class Record {
    private String name;
    private String login;

    //GETters
    public String getName() {
        return name;
    }

    public String getLogin() {
        return login;
    }

    //SETters
    public void setName(String name) {
        this.name = name;
    }

    public void setLogin(String login) {
        this.login = login;
    }

}
